package Aula03;

public class Operações {
    public static void main(String[] args) {

        int num1 = -5 + 8 * 6;
        int num2 = (55 + 9) % 9;
        int num3 = 20 + -3 * 5 / 8;
        int num4 = 5 + 15 / 3 * 2 - 8 % 3;

        System.out.println("a) -5 + 8 * 6 = " + num1);
        System.out.println("b) (55 + 9) % 9 = " + num2);
        System.out.println("c) 20 + -3*5 / 8 = " + num3);
        System.out.println("d) 5 + 15 / 3 * 2 - 8 % 3 = " + num4);
    }
}
